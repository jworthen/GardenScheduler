export function getCategoryBgLight(category: PlantCategory): string {
  const map: Record<PlantCategory, string> = {
    vegetable: 'bg-green-50',
    fruit: 'bg-orange-50',
    herb: 'bg-lime-50',
    'flower-annual': 'bg-pink-50',
    'flower-perennial': 'bg-purple-50',
    bulb: 'bg-yellow-50',
    cutting: 'bg-rose-50',
  };
  return map[category] ?? 'bg-gray-50';
}

